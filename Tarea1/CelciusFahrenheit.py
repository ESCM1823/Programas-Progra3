# Programa que calcule grados celsius a Fahrenheit
gradosCelsius = float(input("Ingresa los grados Celsius: "))#creacion de variable e ingreso de datos

gradosFahrenheit = (gradosCelsius * 9/5) + 32 #formulada

print("\nLa conversión", gradosCelsius, "°C", " a Fahrenheit es: ", gradosFahrenheit, "°F")#salida de datos