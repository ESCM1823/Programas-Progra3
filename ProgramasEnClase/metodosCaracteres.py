#metodo capitalize
texto = "hola mundo"
resultado = texto.capitalize()
print(resultado)

#metodo find
texto = "Hola mundo soy Stevens"
palabra = texto.find("Stevens")
print("\n", palabra)

#metodo isalnum
texto = "Python1234567"
resultado = texto.isalnum()
print("\n", resultado)

#metodo isdigit
texto = "1234567890"
resultado = texto.isdigit()
print("\n", resultado)